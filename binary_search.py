def binary_search(wanted: int, nums: list[int]) -> Tuple[int, int]:
    left = 0
    right = len(nums)
    turns = 0

    while left < right:
        turns += 1
        mid = left + (right - left) // 2

        if nums[mid] == wanted:
            return (mid, turns)

        if nums[mid] > wanted:
            right = mid
        else:
            left = mid + 1

    return (-1, turns)


def run():
    max_val = 0
    for i in range(100):
        num = random.choice(my_nums)
        [idx, took] = binary_search(num, my_nums)

        if took > max_val:
            max_val = took

        print(f"Turn: {i+1}. Index: {idx}. Took: {took}.")

    print(f"max: {max_val} ")


if __name__ == "__main__":
    run()
